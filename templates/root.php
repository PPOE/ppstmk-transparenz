<h5>Parteienförderung & Klubförderung</h5>

<p>
    Seit wir im Gemeinderat sind, bekommen wir Parteienförderung und einen
    kleinen
    Teil Klubförderung bzw. - bei uns von der Bezeichnung passender - Finanzierung der Arbeit der politischen
    Mandatare.
    Bei Kontoüberträgen handelt es sich um Überträge von nicht genutzten bzw. vom Bund verwalteten Konten, die wir
    übernommen haben. Zu den Konten Parteienförderung <a href="?page=2015-parteienf">2015</a>,
    <a href="?page=2016-parteienf">2016</a>, <a href="?page=2017-parteienf">2017</a> und
    Klubförderung <a href="?page=2015-klubf">2015</a>, <a href="?page=2016-klubf">2016</a>, <a href="?page=2017-klubf">2017</a>.
</p>


<h5>Gemeinderatswahl</h5>

<p>
    Dem Fairnessabkommen folgenden finden sich hier alle unseren Ausgaben für die
    Gemeinderatswahl.
    Hervorzuheben ist, dass sich keine Agentur um unseren Wahlkampf gekümmert hat und auch keine Kredite oder
    Darlehen
    aufgenommen wurden. <a href="?page=grazwahl17">Zur Gemeinderatswahl Graz Übersicht 2017 »</a>
</p>


<h4>Dokumente</h4>

<p>
    Hier sind alle relevanten Unterlagen zur Transparenz zu finden. Es finden sich neben dem Fairnessabkommen, alle
    Prüfberichte von Wirtschaftsprüfern sowie alle weiteren Unterlagen rund um die Parteienförderung.
</p>


<h4>Sitzungen und Beschlüsse</h4>

<p>
    Hier finden sich alle unsere Beschlüsse, die bereits ins Wiki übertragen wurden. Es handelt sich hier mehr um
    parteiinterne Abläufe und Beschlüsse (Generalversammlungen, Landesvorstand). Unter Liquid Graz und Steiermark finden
    sich Beschlüsse, die durch die Basis gefasst wurden. Im Liquid Graz findet sich ergänzend der Bereich Gemeinderat,
    wo alle Beschlüsse rund um die Gemeinderatssitzungen zu finden sind. Alle Tagesordnungspunkte im Gemeinderat werden
    hier von uns online gestellt (bevor es durch die Stadt Graz passiert) und gemeinsam im Team diskutiert und
    abgestimmt.
</p>


<h4>Gemeinderatsanträge</h4>

<p>
